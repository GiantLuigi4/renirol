package tfc.renirol.frontend.reni.font;

public class FontSize {
    public final short width, height;
    public final long ppemX, ppemY;

    public FontSize(short width, short height, long ppemX, long ppemY) {
        this.width = width;
        this.height = height;
        this.ppemX = ppemX;
        this.ppemY = ppemY;
    }
}
